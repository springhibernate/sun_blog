function sure_del(btn){
    //alert($(btn).parents("table").html());
    $("table").find('input[type=checkbox]').attr('checked', false);
    $(btn).parent().find('input[type=checkbox]').attr('checked', true);
    return confirm('确定要删除吗？');
}
/**
* 删除左右两端的空格
*/
String.prototype.trim=function()
{
     return this.replace(/(^\s*)|(\s*$)/g, "");
}
/**
* 删除左边的空格
*/
String.prototype.ltrim=function()
{
     return this.replace(/(^\s*)/g,"");
}
/**
* 删除右边的空格
*/
String.prototype.rtrim=function()
{
     return this.replace(/(\s*$)/g,"");
}

function changeLang(selObj){
    var sel_lang=selObj.options[selObj.selectedIndex].value;
    $.post("/blog-list.aspx?changeLang=1",
        {sel_lang:sel_lang},
        function(){
            window.location="/blog-list.aspx";
        }
    );
}
function add_form_css(){
    //the form(generated by helper form_for)'s class is fixed(new_model or edit_model),here use js to change
    //ps:can change as follow:form_for(@comment,:html=>{:class=>'form-horizontal'})
    var old_css=$("form").attr("class");
    $("form").attr("class",old_css+" form-horizontal");
}
function boostrap_paging(){
    //the paging label(generated by helper will_paginate) does't corresponse to boostrap,here use js to change
    $(".list-group-item div").children().each(function (index,child) {
        if(child.tagName=="EM"){
            $(child).wrap("<li class='active'><span><span></li>");
        }else if($(child).attr("class")=="previous_page disabled" || $(child).attr("class")=="next_page disabled"){
            $(child).wrap("<li class='disabled'></li>");
        }else{
            $(child).wrap("<li></li>");
        }
    });
}

$(function(){
    //add_form_css();
    boostrap_paging();
});